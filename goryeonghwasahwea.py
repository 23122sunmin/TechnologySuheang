# 해결법을 보여주는 함수
def show_solution(menu):
    solution = solutions.get(menu, "해당 항목에 대한 정보가 없습니다.")
    messagebox.showinfo("해결법", solution)

# Tkinter 애플리케이션 생성
root = tk.Tk()
root.title("노인 복지 공무원 가이드")
root.geometry("400x500")

# 안내 메시지
label = tk.Label(root, text="이것을 클릭하여 현재 필요한 가이드를 찾아보세요", font=("Arial", 12), wraplength=350)
label.pack(pady=10)

# 버튼 생성
for key in solutions.keys():
    button = tk.Button(root, text=key, font=("Arial", 10), command=lambda k=key: show_solution(k))
    button.pack(pady=5, fill=tk.X)

# 메인 루프 실행
root.mainloop()
